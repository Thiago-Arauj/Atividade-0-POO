public class Atividade_0_Question1 {
    public static void main(String[] args){
        int a = 10;
        int b = 30;

        // Transformando as entradas inteiras em double para que a divisão exata seja sempre possível
        double c = a;
        double d = b;


        int product = a * b;
        double division = c / d;
        int addition = a + b;
        int subtraction = a - b;

        System.out.println("Soma = " + addition + "\nSubtração = " + subtraction +
                "\nProduto = " + product + "\nDivisão = " + division);

    }
}
